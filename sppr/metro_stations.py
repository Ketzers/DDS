CITY_CENTER_LAT = 55.7558  # Красная площадь
CITY_CENTER_LON = 37.6173

# Радиусы геозон от центра (км) и соответствующие коэффициенты аренды
# относительно медианы рынка. Источник: усреднённые данные ЦИАН/Авито
# по аренде коммерческой недвижимости 150–200 м² в Москве (декабрь 2025).
# Это ОЦЕНОЧНЫЕ значения для прототипа, не точные ставки.
RENT_ZONES = [
    # (max_distance_km, zone_name, rent_coef, description)
    ( 3.0, 'ЦАО (внутри Садового)',     1.80, 'центр в пределах Садового кольца'),
    ( 6.0, 'ЦАО (вне Садового)',        1.40, 'центральная часть города'),
    (10.0, 'Ближний пояс',              1.10, 'между Садовым и ТТК — близко к центру'),
    (15.0, 'Средний пояс',              0.85, 'между ТТК и МКАД'),
    (25.0, 'Дальний пояс',              0.65, 'окраины МКАД и ближнее Подмосковье'),
    (999.0, 'Периферия',                0.50, 'дальняя периферия'),
]


def haversine_km(lat1: float, lon1: float, lat2: float, lon2: float) -> float:
    """Расстояние в км между двумя географическими точками."""
    R = 6371.0
    lat1_r, lat2_r = math.radians(lat1), math.radians(lat2)
    dlat = math.radians(lat2 - lat1)
    dlon = math.radians(lon2 - lon1)
    a = math.sin(dlat/2)**2 + math.cos(lat1_r) * math.cos(lat2_r) * math.sin(dlon/2)**2
    return 2 * R * math.asin(math.sqrt(a))


def get_rent_zone(lat: float, lon: float) -> tuple[str, float, str]:
    """Определяет зону аренды по координатам.

    Returns:
        (zone_name, rent_coefficient, description)
    """
    d = haversine_km(lat, lon, CITY_CENTER_LAT, CITY_CENTER_LON)
    for max_d, name, coef, desc in RENT_ZONES:
        if d <= max_d:
            return (name, coef, desc)
    return RENT_ZONES[-1][1:]  # на всякий случай


def find_station(name: str) -> Optional[MetroStation]:
    """Поиск станции по точному названию."""
    for s in METRO_STATIONS:
        if s.name == name:
            return s
    return None


def get_station_names() -> list[str]:
    """Список всех названий станций (для UI multiselect)."""
    return sorted([s.name for s in METRO_STATIONS])


def find_quiet_stations(quests_df, radius_km: float = 0.9,
                         max_competitors: int = 0,
                         exclude_far_periphery: bool = True,
                         max_distance_from_center_km: float = 25.0,
                         exclude_central: bool = False,
                         min_distance_from_center_km: float = 0.0) -> list[tuple[MetroStation, int]]:
    """Находит станции метро, рядом с которыми мало или нет квестов.

    Args:
        quests_df: датафрейм квестов (нужны колонки lat, lon)
        radius_km: радиус поиска конкурентов
        max_competitors: максимальное число конкурентов в радиусе
        exclude_far_periphery: исключить станции дальше N км от центра
        max_distance_from_center_km: лимит «далеко от центра»
        exclude_central: исключить станции внутри Садового кольца
            (нужно при ограниченном датасете в топ-N квестов, чтобы не предлагать
            «свободные» центральные станции, которые на самом деле забиты)
        min_distance_from_center_km: минимальное расстояние до центра (для тонкой настройки)

    Returns:
        Список (станция, число_конкурентов_в_радиусе),
        отсортированный по числу конкурентов, потом по расстоянию до центра.
    """
    quiet = []
    for s in METRO_STATIONS:
        d_center = haversine_km(s.lat, s.lon, CITY_CENTER_LAT, CITY_CENTER_LON)

        if exclude_far_periphery and d_center > max_distance_from_center_km:
            continue
        if exclude_central and s.in_central:
            continue
        if d_center < min_distance_from_center_km:
            continue

        # Считаем число квестов в радиусе (векторно через numpy-like операции)
        lats = quests_df['lat'].values
        lons = quests_df['lon'].values
        dlat_km = (lats - s.lat) * 111.0
        dlon_km = (lons - s.lon) * 111.0 * math.cos(math.radians(s.lat))
        dists = (dlat_km**2 + dlon_km**2) ** 0.5
        n_competitors = int((dists <= radius_km).sum())

        if n_competitors <= max_competitors:
            quiet.append((s, n_competitors, d_center))

    # Сортировка: меньше конкурентов → ближе к центру
    quiet.sort(key=lambda x: (x[1], x[2]))
    return [(s, n) for s, n, _ in quiet]


