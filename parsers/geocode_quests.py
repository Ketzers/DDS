def clean_address(raw_address):
    """Готовит строку адреса к запросу: убирает префикс «м. ...,»
    и подставляет город, если его нет в строке."""
    addr = re.sub(r'^м\.\s*[^,]+,\s*', '', raw_address).strip()
    if 'Москва' not in addr and 'Котельники' not in addr:
        addr = f"Москва, {addr}"
    return addr


def extract_metro(metro_raw):
    """Возвращает чистое название станции без префикса «м.»."""
    if pd.isna(metro_raw):
        return ""
    return str(metro_raw).replace('м.', '').strip()


def geocode_address(address, retries=3):
    """Запрос к Nominatim. Возвращает (lat, lon) или (None, None)."""
    for attempt in range(retries):
        try:
            location = geolocator.geocode(address, exactly_one=True)
            if location:
                return (location.latitude, location.longitude)
            return (None, None)
        except GeocoderTimedOut:
            print(f"    Timeout, повтор {attempt + 1}/{retries}...")
            time.sleep(2)
        except GeocoderServiceError as e:
            print(f"    Service error: {e}")
            return (None, None)
    return (None, None)


def main():
    print("=" * 60)
    print("Геокодинг квест-комнат Москвы")
    print("=" * 60)

    df = pd.read_excel(INPUT_FILE)
    print(f"Загружено {len(df)} квестов\n")

    lats, lons, sources = [], [], []
    success, fallback, failed = 0, 0, 0

    for idx, row in df.iterrows():
        raw_addr  = str(row.get('Адрес', ''))
        raw_metro = str(row.get('Метро', ''))
        name      = str(row.get('Название', f'#{idx}'))

        # Стратегия 1: запрос к Nominatim по полному адресу
        clean_addr = clean_address(raw_addr)
        print(f"[{idx + 1:3d}/{len(df)}] {name[:30]:<30} -> {clean_addr[:45]}", end="  ")

        lat, lon = geocode_address(clean_addr)

        if lat is not None:
            print(f"OK ({lat:.4f}, {lon:.4f})")
            lats.append(lat); lons.append(lon); sources.append('address')
            success += 1
        else:
            # Стратегия 2: фолбэк — координаты ближайшей станции метро
            metro_name   = extract_metro(raw_metro)
            metro_coords = METRO_COORDS.get(metro_name)

            if metro_coords:
                lat, lon = metro_coords
                print(f"фолбэк по метро «{metro_name}» ({lat:.4f}, {lon:.4f})")
                lats.append(lat); lons.append(lon); sources.append('metro_fallback')
                fallback += 1
            else:
                print("не найдено")
                lats.append(None); lons.append(None); sources.append('failed')
                failed += 1

        time.sleep(DELAY)

    df['lat']        = lats
    df['lon']        = lons
    df['geo_source'] = sources
    df.to_excel(OUTPUT_FILE, index=False)

    print(f"\n{'=' * 60}")
    print(f"Готово. Результат сохранён в {OUTPUT_FILE}")
    print(f"  По адресу:     {success}")
    print(f"  По метро:      {fallback}")
    print(f"  Не найдено:    {failed}")
    print('=' * 60)

    # Список записей, требующих проверки
    problems = df[df['geo_source'] != 'address'][['Название', 'Адрес', 'Метро', 'geo_source']]
    if not problems.empty:
        print("\nЗаписи, геокодированные не по адресу:")
        print(problems.to_string())


if __name__ == '__main__':
    main()
